# Variables are basically created at the moment when we declare it with it's value

x = 2     # type = int
y = "Python"    # type = str
print(x)
print(y)


# To get the type of an variable

print(type(x))    # <class 'int'>
print(type(y))    # <class 'str'>


# Assign using single Quotes '

y = "Python"

# Assign using double Quotes "

y = 'Python'



# Case Sensitive

a = "It is a value of a"
A = "It is a value of A"

print(a)    # It is a value of a
print(A)    # It is a value of A
